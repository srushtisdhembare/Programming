## Application which accepts a no. and returns its factorial
##
##################################################################################################

def Factorial(no):
    fact = 1
    for j in range(1,no+1):
        fact = fact * j
    return fact


def main():
    Value = int(input("Enter a number :"))
    Ret = Factorial(Value)
    print("Factorial is : ",Ret)

##################################################################################################
##
## Starter Function
##
##################################################################################################

if __name__ == "__main__":
    main()

