def DisplayNos(No):
    for no in range(1,No+1):
            print(no)   
    
#########################################################################################
##
## Application that accepts a no. and Prints that many nos starting from 1
##
#########################################################################################

def main():
    print("Enter number:")
    Value = int(input())
    
    DisplayNos(Value)

#########################################################################################
##
## Starter Function
##
#########################################################################################

if __name__ == "__main__":                 
    main()

