def AreaRect(No1 , No2): return No1 * No2   
    
#########################################################################################
##
## Application that accepts length and width of rectangle & prints area
##
#########################################################################################

def main():
    Length = int(input("Enter length:"))
    Width = int(input("Enter width:"))
    
    Ret = AreaRect(Length , Width)

    print("Area of a rectangle is :",Ret)

#########################################################################################
##
## Starter Function
##
#########################################################################################

if __name__ == "__main__":                 
    main()

