def Grade(No):
    if(No < 50) : print("Fail")
    elif(No >= 50 and No < 60): print("Second Class")
    elif(No >= 60 and No < 75): print("First Class")
    else: print("Distinction")
    
#########################################################################################
##
## Application that accepts marks and displays grade
##
#########################################################################################

def main():
    print("Enter marks:")
    Value = int(input())

    Grade(Value)
    
