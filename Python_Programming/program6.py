def Table(No):
    for no in range(1,10):
        print(No * no)
    
#########################################################################################
##
## Application that accepts a no. and prints its multiplication table
##
#########################################################################################

def main():
    print("Enter number:")
    Value = int(input())

    Table(Value)


#########################################################################################
##
## Starter Function
##
#########################################################################################

if __name__ == "__main__":                 
    main()

