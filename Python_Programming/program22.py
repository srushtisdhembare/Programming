def AreaRect(No): return (3.14 * No * No)   
    
#########################################################################################
##
## Application that accepts radius of circle and prints its area
##
#########################################################################################

def main():
    Radius = int(input("Enter radius:"))
    
    Ret = float(AreaRect(Radius))

    print("Area of a circle is :",Ret)

