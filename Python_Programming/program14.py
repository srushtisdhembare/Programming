def ReverseDigits(No):
    Digit = 0
    No2 = 0
    
    while(No != 0):
        No2 = No2*10
        Digit = No % 10         
        No = int(No/10)         
        No2 = No2 + Digit      
                        
    return No2   
    
#########################################################################################
##
## Application that accepts a no. and Prints reverse of digits in that no.
##
#########################################################################################

def main():
    print("Enter number:")
    Value = int(input())
    
    Ret = ReverseDigits(Value)

    print("Reverse of digits : ", Ret)

