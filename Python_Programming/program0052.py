## Application which accepts a no. and returns true if no. divisible by 5 else returns false 
##
##################################################################################################
def CheckDivisible(no):
    if(no % 5 == 0):
        return True
    else:
        return False

def main():
    Value = int(input("Enter number :"))
    Ret = CheckDivisible(Value)

    print(Ret)
    
 

