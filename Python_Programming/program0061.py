## Application which accepts a no. and prints matrix
##
##################################################################################################

def Display():
    for j in range(5,0,-1):
        for i in range(0,5):
            if(j > i):
                print("*", end = "\t")
        print(end = "\n")


def main():
    Display()

##################################################################################################
##
## Starter Function
##
##################################################################################################

if __name__ == "__main__":
    main()

