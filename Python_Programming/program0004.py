def Cube(No):
    return No * No * No
    
#########################################################################################
##
## Application that accepts a no. and prints cube of that no.
##
#########################################################################################

def main():
    print("Enter number:")
    Value = int(input())

    Ret = Cube(Value)

    print(Ret ," is its cube")

#########################################################################################
##
## Starter Function
##
#########################################################################################

if __name__ == "__main__":                 
    main()

