## Application which contains one lambda function which accepts 1 parameter and returns power of 2
##
##################################################################################################
def main():
    print("Enter number :")
    no = int(input())

    Power = lambda x : x * x

    Ans = Power(no)

    print(Ans)

##################################################################################################
##
## Starter Function
##
##################################################################################################

if __name__ == "__main__":
    main()

