## Application which accepts a no. and checks whether it is positive or negative or zero  
##
##################################################################################################
def CheckNum(no):
    if(no == 0):
        print("Zero")
    elif(no < 0):
        print("Negative Number")
    else:
        print("Positive Number")

def main():
    Value = int(input("Enter number :"))
    CheckNum(Value)
    
 

##################################################################################################
##
## Starter Function
##

##################################################################################################

if __name__ == "__main__":                 
    main()


