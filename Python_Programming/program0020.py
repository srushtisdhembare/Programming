def DisplayNosReverse(No):
    for no in range(No, 0 , -1):
            print(no)   
    
#########################################################################################
##
## Application that accepts a no. and Prints that many nos in Reverse order
##
#########################################################################################

def main():
    print("Enter number:")
    Value = int(input())
    
    DisplayNosReverse(Value)

#########################################################################################
##
## Starter Function
##
#########################################################################################

if __name__ == "__main__":                 
    main()

