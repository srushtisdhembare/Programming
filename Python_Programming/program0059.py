## Application which accepts a no. and returns addition of its factors
##
##################################################################################################

def FactorAdd(no):
    sum = 0
    for i in range(1,no):
        if(no % i == 0):
            sum = sum + i
    return sum


def main():
    Value = int(input("Enter a number :"))
    Ret = FactorAdd(Value)
    print("Factor addition is : ",Ret)

