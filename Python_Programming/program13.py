def SumDigits(No):
    Sum = 0
    Digit = 0
    
    while(No != 0):
        Digit = No % 10
        Sum = Sum + Digit
        No = int(No/10)
        
    return Sum    
    
#########################################################################################
##
## Application that accepts a no. and Prints sum of digits in that no.
##
#########################################################################################

def main():
    print("Enter number:")
    Value = int(input())
    
    Ret = SumDigits(Value)

    print("Sum of digits are : ", Ret)

