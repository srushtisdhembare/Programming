def Add(No):
    Sum = 0
    for no in range(1, No + 1):
        Sum = Sum + no
    return Sum
    
#########################################################################################
##
## Application that accepts a no. and prints sum of first N natural nos
##
#########################################################################################

def main():
    print("Enter number:")
    Value = int(input())

    Ret = Add(Value)
    print("Sum is :", Ret)


#########################################################################################
