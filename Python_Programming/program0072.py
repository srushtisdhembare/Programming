##
## Application which contains one lambda function which accepts 2 parameter and returns multiplication
##
##################################################################################################
def main():
    print("Enter first number :")
    no1 = int(input())

    print("Enter second number :")
    no2 = int(input())

    Mult = lambda x , y: x * y

    Ans = Mult(no1 , no2)

    print("Multiplication is : ",Ans)

##################################################################################################
##
## Starter Function
##
##################################################################################################

if __name__ == "__main__":
    main()

