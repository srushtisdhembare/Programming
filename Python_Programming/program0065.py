## Application which accepts a no. and returns addition of digits in that no
##
##################################################################################################

def AddDigit(no):
    Sum = 0
    while(no != 0):
        Digit = no % 10
        Sum = Sum + Digit
        no = int(no / 10)
    return Sum

def main():
    Value = int(input("Enter a number : "))
    Ret = AddDigit(Value)
    print("Summation of digits are : ",Ret)

