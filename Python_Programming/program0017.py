def DisplayFactors(No):
    for no in range(1,No+1):
        if(No % no == 0):
            print(no)   
    
#########################################################################################
##
## Application that accepts a no. and Prints factors of the no.
##
#########################################################################################

def main():
    print("Enter number:")
    Value = int(input())
    
    DisplayFactors(Value)

#########################################################################################
##
## Starter Function
##
#########################################################################################

if __name__ == "__main__":                 
    main()

