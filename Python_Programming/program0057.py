## Application which accepts a no. and prints matrix
##
##################################################################################################

def Display(no):
    for j in range(0,no):
        for i in range(0,no):
            print("*", end = "\t")
        print(end = "\n")


def main():
    Value = int(input("Enter a number :"))
    Display(Value)

##################################################################################################
##
## Starter Function
##
##################################################################################################

if __name__ == "__main__":
    main()

