def DisplayEven(No):
    for no in range(1,No +1):
        if(no % 2 == 0):
            print(no)
    
#########################################################################################
##
## Application that accepts a no. and prints all even nos till that no.
##
#########################################################################################

def main():
    print("Enter number:")
    Value = int(input())

    DisplayEven(Value)


#########################################################################################
##
## Starter Function
##
#########################################################################################

if __name__ == "__main__":                 
    main()

