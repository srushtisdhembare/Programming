##  Function name : Factorial
##  Input         : int 
##  Output        : int 
##  Description   : Calculates factorial of given no.
##  Date          : 29/06/2026
##  Author        : Srushti Sachin Dhembare
##
#########################################################################################

def Factorial(No):
    Fact = 1
    for no in range(1 , No + 1):
        Fact = Fact * no
    return Fact
    
#########################################################################################
##
## Application that accepts a no. and prints factorial of given no.
##
#########################################################################################

def main():
    print("Enter number:")
    Value = int(input())

    Ret = Factorial(Value)
    print("Factorial of number is :",Ret)


#########################################################################################
##
## Starter Function
##
#########################################################################################

if __name__ == "__main__":                 
    main()

