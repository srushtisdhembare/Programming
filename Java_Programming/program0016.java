import java.util.*;

class program19
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.SumEvenNumbers(10);   
    }

}

class Logic
{
    void SumEvenNumbers(int N)
    {
        int iCnt = 0, iSum = 0;

        for(iCnt = 1; iCnt <= N ; iCnt++)
        {
            if(iCnt % 2 == 0)
            {
                iSum = iSum + iCnt;
            }
        }
       
        System.out.println(iSum);
    }
}


