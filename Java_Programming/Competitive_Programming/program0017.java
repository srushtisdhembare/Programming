// Application to print nos from N to 1 in reverse order
//
/////////////////////////////////////////////////////////////////////////////////////////

import java.util.*;

class program19
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.PrintReverse(10);   
    }

}

class Logic
{
    void PrintReverse(int N)
    {
        int iCnt = 0;

        for(iCnt = N; iCnt > 0 ; iCnt--)
        {
            System.out.print(iCnt+"\t");
        }    
    }
}


