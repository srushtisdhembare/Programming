import java.util.*;

class program17
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.CheckPrime(11);   
    }

}

class Logic
{
    void CheckPrime(int num)
    {
        int iCnt = 0;

        for(iCnt = 2; iCnt <= num/2; iCnt++)
        {
            if((num % iCnt) == 0)
            {
                System.out.println("It is not a prime number");
            }
        }
        System.out.println("It is  a prime number");
    }
}


