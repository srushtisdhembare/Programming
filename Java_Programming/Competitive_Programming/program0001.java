import java.util.*;

class program16
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.SumOfDigits(1234);   
    }

}

class Logic
{
    void SumOfDigits(int num)
    {
        int iDigit = 0, iSum = 0;

        while(num != 0)
        {
            iDigit = num % 10;
            iSum = iSum + iDigit;
            num = num/10; 
        }
        System.out.println("Sum of Digits is :"+iSum);
    }
}


