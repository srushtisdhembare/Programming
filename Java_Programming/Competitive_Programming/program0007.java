import java.util.*;

class program16
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.PrintEvenNos(20);   
    }

}

class Logic
{
    void PrintEvenNos(int num)
    {
        int iCnt = 0;

        for(iCnt = 1; iCnt <= num; iCnt++)
        {
            if((iCnt %  2) == 0)
            {
                System.out.print(iCnt+"\t");
            }
        }
        
    }
}


