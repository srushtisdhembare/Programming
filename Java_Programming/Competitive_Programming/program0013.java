import java.util.*;

class program18
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.CheckDivisible(55);   
    }

}

class Logic
{
    void CheckDivisible(int num)
    {
        if((num % 5 == 0) && (num % 11 == 0))
        {
            System.out.println("Divisible by 5 and 11");
        }
        else
        {
           System.out.println("Not Divisible by 5 and 11"); 
        }    
    }
}


