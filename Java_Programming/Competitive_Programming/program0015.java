import java.util.*;

class program18
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.CalculatePower(2,5);   
    }

}

class Logic
{
    void CalculatePower(int base, int exp)
    {
        int iCnt = 0, result =1;

        for(iCnt = 1; iCnt <= exp; iCnt++)
        {
            result = result * base;
        }
       
        System.out.println(result);
    }
}


