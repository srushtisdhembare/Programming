import java.util.*;

class program16
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.PrintTable(5);   
    }

}

class Logic
{
    void PrintTable(int num)
    {
        int iCnt = 0;

        for(iCnt = 1; iCnt <= 10; iCnt++)
        {
            System.out.print(num * iCnt+"\t");
        }
       
    }
}


