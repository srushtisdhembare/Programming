import java.util.*;

class program16
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.FindMin(3, 7, 2);   
    }

}

class Logic
{
    void FindMin(int a, int b, int c)
    {
       int iArr[] = {a ,b ,c};
       int iCnt = 0, iMin = iArr[0];

       for(iCnt = 0; iCnt < 3; iCnt++)
       {
            if(iArr[iCnt] <= iMin)
            {
                iMin = iArr[iCnt];
            }
       }
       System.out.println("Minimum number is : "+iMin);
    }
}


