import java.util.*;

class program18
{
    public static void main(String A[])
    {
        Logic lobj = new Logic();
        lobj.CheckLeapYear(2024);   
    }

}

class Logic
{
    void CheckLeapYear(int num)
    {
        if((num % 4 == 0))
        {
            System.out.println("It is  a leap year");
        }
        else
        {
            System.out.println("It is not a leap year");
        }
        
        
    }
}


