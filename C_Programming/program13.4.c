/////////////////////////////////////////////////////////////////////////////////////////
//
//  Function name : Count
//  Input         : array , int
//  Output        : int
//  Description   : return frequency of 11
//  Date          : 02/07/2026
//  Author        : Srushti Sachin Dhembare
//
