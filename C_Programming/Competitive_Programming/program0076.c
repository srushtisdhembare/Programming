void Pattern(int iNo)
{
    int iCnt = 0;
    char cLetter = 'A';

    for(iCnt = 1; iCnt <= iNo; iCnt++)
    {
        printf("%c\t",cLetter);
        cLetter++;
    }
}

/////////////////////////////////////////////////////////////////////////////////////////
//
// Application to Accept number and print pattern
//
/////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;

    printf("Enter number of elements :\n");
    scanf("%d",&iValue);

    Pattern(iValue);

    return 0;
}

